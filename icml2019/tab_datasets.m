%% 
% basic statistics of used datasets
%

dataSet = {...
    'avila1', ...
    'codrna1',...
    'covtype1',...
    'ijcnn1',...
    'letter1', ...
    'pendigit1',...
    'phishing1',...
    'sattelite1',...
    'sensorless1',...
    'shuttle1', ...
    };


fprintf('                     #lab  #feat  #examples     trn1     val1     trn2     val2      tst \n');
for i = 1 : numel( dataSet )
    Data = load( ['../data/' dataSet{i} '.mat'] );
    
    nDims = size(Data.X,1);
    nY    = max(Data.Y);
    
    nTrn1 = [];
    nTrn2 = [];
    nVal1 = [];
    nVal2 = [];
    nTst  = [];
    for s = 1 : numel( Data.Split )
        nTrn1(s) = numel( Data.Split(s).trn1);
        nTrn2(s) = numel( Data.Split(s).trn2);
        nVal1(s) = numel( Data.Split(s).val1);
        nVal2(s) = numel( Data.Split(s).val2);
        nTst(s) = numel( Data.Split(s).tst);
    end
    
    fprintf('%20s   %2d   %4d    %6d    %6d   %6d   %6d   %6d   %6d\n', dataSet{i}, nY, nDims, ...
        sum( [mean(nTrn1), mean(nVal1),mean(nTrn2), mean(nVal2), mean(nTst)]),...
        mean(nTrn1), mean(nVal1),mean(nTrn2), mean(nVal2), mean(nTst));
end